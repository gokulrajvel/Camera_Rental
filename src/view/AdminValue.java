package view;

public class AdminValue {
    private String adminId;
    private String pw;

    public void setAdminId(String adminId) {
        this.adminId = adminId;
    }

    public void setPassword(String pw) {
        this.pw = pw;
    }

    public String getAdminId() {
        return adminId;
    }

    public String getPassword() {
        return pw;
    }
}
